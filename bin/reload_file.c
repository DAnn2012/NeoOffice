#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <mach-o/dyld.h>

int main(int argc, char **argv)
{
	if ( argc != 2 || access( argv[1], R_OK ) != 0 )
	{
		fprintf( stderr, "Usage: %s file\n", argv[0] );
		exit(1);
	}

	if ( NSAddImage( argv[1], NSADDIMAGE_OPTION_RETURN_ON_ERROR ) )
		exit(0);

	exit(1);
}
